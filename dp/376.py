from typing import List


# leetcode submit region begin(Prohibit modification and deletion)
class Solution:
    def wiggleMaxLength(self, nums: List[int]) -> int:
        nums_len = len(nums)
        if nums_len <= 1:
            return nums_len
        dp = [1] * nums_len
        flag = ""
        last_flag = ""
        for i in range(1, nums_len):
            difference = nums[i] - nums[i - 1]
            if difference == 0:
                dp[i] = dp[i - 1]
                continue
            if difference < 0:
                flag = "-"
            elif difference > 0:
                flag = "+"
            if flag != last_flag:
                dp[i] = dp[i - 1] + 1
                last_flag = flag
            else:
                dp[i] = dp[i - 1]

        return dp[-1]


