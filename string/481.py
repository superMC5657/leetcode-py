from typing import List


# leetcode submit region begin(Prohibit modification and deletion)
class Solution:
    def magicalString(self, n: int) -> int:
        s = "122"
        i = 2
        while n > len(s) > i:
            if i % 2 == 0:
                s += "1" * int(s[i])
                i += 1
            else:
                s += "2" * int(s[i])
                i += 1
        return s[:n].count('1')


